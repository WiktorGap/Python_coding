git branch wyświetla lista lokalnych gałęzi
git branch [nazwa] dodaje brancz nazwa
git branch -d[nazwa] uswa brancz o danej nazwia
git branch -m[stara nazwa] [nowa nazwa]
git branch -a wyswietli loklane i zdalne galezie
git branch -r wyswietli zdalne galezie
git branch --show-current
git branch -v commity na wszystkich galeziach wiswietla
git branch --merged wyswietla zmergowanie gałęzie z bieżącej gałęzi
git branch --no-merged 
git branch -vv szczegółowe informacje o każdej gałęzi